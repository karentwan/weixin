package com.weixin.pojo;

/**
 * 签到和未签到的信息封装
 * @author wan
 */
public class Sign {

	private String name;
	
	private String account;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAccount() {
		return account;
	}

	public void setAccount(String account) {
		this.account = account;
	}
	
	
}
